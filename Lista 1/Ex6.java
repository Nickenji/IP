/*	Escreva um programa que converta um valor em real (em uma variável), para dólar. A
	taxa de conversão real para dólar também é um dado do programa.
*/
class Ex6 {
	public static void main(String[] args) {
		final double dolar = 0.32145; // Valor de um real em dolar.
		double real = 100; // Valor do real que voce deseja converter.
	
		System.out.println("Convertendo " + real + " reais em dolar, temos: " + real * dolar + " dolares");
	
	}
}
	

	
	
	
	
	

